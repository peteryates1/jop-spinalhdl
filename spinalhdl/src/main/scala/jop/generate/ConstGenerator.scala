         |	public static final int IO_CPUCNT = IO_BASE + ${ioOffset(JopIoSpace.SYS_BASE + 11)};
         |	/** Performance counter */
         |	public static final int IO_PERFCNT = IO_BASE + ${ioOffset(JopIoSpace.SYS_BASE + 12)};
