         |  it under the terms of the GNU General Public License as published by
         |  the Free Software Foundation, either version 3 of the License, or
         |  (at your option) any later version.
         |
         |  This program is distributed in the hope that it will be useful,
         |  but WITHOUT ANY WARRANTY; without even the implied warranty of
         |  MERCHANTABILITY or FITNESS FOR A PARTICULAR PURPOSE.  See the
         |  GNU General Public License for more details.
         |
         |  You should have received a copy of the GNU General Public License
         |  along with this program.  If not, see <http://www.gnu.org/licenses/>.
         |*/
         |
         |// Generated by jop.generate.ConstGenerator from JopConfig
         |// Assembly: ${config.assembly.name}
         |// Do not edit manually — regenerate with:
         |//   sbt "runMain jop.generate.ConstGeneratorMain <preset> --write"
         |
         |package com.jopdesign.sys;
         |
         |/**
         | * Constants for the class structure, hardware configuration,
         | * and IO addresses.
         | *
         | * @author martin
         | */
         |public class Const {
         |
         |	// ====================================================================
         |	// Class structure constants (architectural, never change)
         |	// ====================================================================
         |
         |	/** Size of class header part (difference between class struct and method table) */
         |	static final int CLASS_HEADR = 5;
         |	/** Size of instance relative to class struct */
         |	public static final int CLASS_SIZE = 0;
         |	/** Pointer to super class relative to class struct */
         |	public static final int CLASS_SUPER = 3;
         |	/** Interface table pointer relative to class struct */
         |	static final int CLASS_IFTAB = 4;
         |	/** Class info start relative to start of MTAB */
         |	public static final int MTAB2CLINFO = -5;
         |	/** GC_INFO field relative to start of MTAB */
         |	static final int MTAB2GC_INFO = -3;
         |
         |	// ====================================================================
         |	// Stack configuration
         |	// ====================================================================
         |
         |	/** Size of the on-chip stack cache including microcode scratch area */
         |	public static final int STACK_SIZE = 65536;
         |	/** Offset of the real stack in the on-chip RAM (set in jvm.asm) */
         |	public static final int STACK_OFF = 64;
         |	/** Constant pool pointer offset in on-chip stack cache */
         |	public static final int RAM_CP = 1;
         |	/** Start address of scratchpad RAM */
         |	public static final int SCRATCHPAD_ADDRESS = 0x400000;
         |
         |	// ====================================================================
         |	// Feature flags (derived from JopCoreConfig)
         |	// ====================================================================
         |
         |	/**
         |	 * True if software float support is needed (any core uses Java for float ops).
         |	 * When false, SoftFloat32 code is dead and eliminated by javac.
         |	 */
         |	public static final boolean SUPPORT_FLOAT = $supportFloat;
         |	/**
         |	 * True if software double support is needed.
         |	 * Always true — no hardware double support yet.
         |	 */
         |	public static final boolean SUPPORT_DOUBLE = true;
         |	/** RTTM is not implemented in SpinalHDL JOP */
         |	public static final boolean USE_RTTM = false;
         |
         |	// ====================================================================
         |	// Exception numbers (architectural)
         |	// ====================================================================
         |
         |	/** Hardware exception: stack overflow */
         |	public static final int EXC_SPOV = 1;
         |	/** Hardware exception: null pointer */
         |	public static final int EXC_NP = 2;
         |	/** Hardware exception: array bounds */
         |	public static final int EXC_AB = 3;
         |	/** Hardware exception: RTTM rollback (unused) */
         |	public static final int EXC_ROLLBACK = 4;
         |	/** Hardware exception: illegal monitor state */
         |	public static final int EXC_MON = 5;
         |	/** Software exception: divide by zero */
         |	public static final int EXC_DIVZ = 8;
         |
         |	// ====================================================================
         |	// I/O base address
         |	// ====================================================================
         |
         |	/**
         |	 * Base address for all I/O devices. Negative value for fast bipush load.
         |	 * Decoded from address bits — 8 device slots, up to 16 registers each.
         |	 */
         |	public static final int IO_BASE = -128; // 0xFFFFFF80
         |
         |	// ====================================================================
         |	// System registers (Sys, always present, fixed at 0xF0)
         |	// ====================================================================
         |
         |	/** System counter (read) / Global interrupt enable (write) */
         |	public static final int IO_CNT = IO_BASE + ${hexOffset(ioOffset(JopIoSpace.SYS_CNT))};
