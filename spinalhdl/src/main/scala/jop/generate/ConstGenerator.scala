         |	public static final int IO_TIMER = IO_US_CNT;
         |	/** Software interrupt trigger (write) / Interrupt number (read) */
         |	public static final int IO_SWINT = IO_BASE + ${ioOffset(JopIoSpace.SYS_BASE + 2)};
         |	/** Interrupt number (alias for IO_SWINT read) */
         |	public static final int IO_INTNR = IO_SWINT;
         |	/** Watchdog */
         |	public static final int IO_WD = IO_BASE + ${ioOffset(JopIoSpace.SYS_WD)};
         |	/** Exception register */
         |	public static final int IO_EXCPT = IO_BASE + ${ioOffset(JopIoSpace.SYS_EXC)};
         |	/** Lock acquire */
         |	public static final int IO_LOCK = IO_BASE + ${ioOffset(JopIoSpace.SYS_LOCK)};
         |	/** Lock release (write) / CPU ID (read) */
         |	public static final int IO_UNLOCK = IO_BASE + ${ioOffset(JopIoSpace.SYS_CPU_ID)};
         |	/** CPU ID (alias for IO_UNLOCK read) */
         |	public static final int IO_CPU_ID = IO_UNLOCK;
         |	/** Signal register */
         |	public static final int IO_SIGNAL = IO_BASE + ${ioOffset(JopIoSpace.SYS_SIGNAL)};
         |	/** Interrupt mask for individual interrupts */
         |	public static final int IO_INTMASK = IO_BASE + ${ioOffset(JopIoSpace.SYS_BASE + 8)};
