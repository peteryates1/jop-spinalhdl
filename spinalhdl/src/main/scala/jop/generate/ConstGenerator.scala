         |	public static final int IO_FPU_CAP = IO_BASE + ${ioOffset(JopIoSpace.SYS_BASE + 15)};
         |
         |	// ====================================================================
         |	// Boot device registers (UART or cfgFlash, fixed at 0xEE)
         |	// ====================================================================
         |
         |	/** UART status register */
         |	public static final int IO_STATUS = IO_BASE + ${hexOffset(ioOffset(JopIoSpace.UART_STATUS))};
         |	/** UART data register */
         |	public static final int IO_UART = IO_BASE + ${hexOffset(ioOffset(JopIoSpace.UART_DATA))};
         |	/** UART status (alias) */
         |	public static final int IO_UART_STATUS = IO_STATUS;
         |	/** UART data (alias) */
         |	public static final int IO_UART_DATA = IO_UART;
         |	/** UART TX data register empty mask */
         |	public static final int MSK_UA_TDRE = 1;
         |	/** UART RX data register full mask */
         |	public static final int MSK_UA_RDRF = 2;
         |""".stripMargin)

    // Peripheral I/O addresses (from IoAddressAllocator)
    sb.append(
      s"""
         |	// ====================================================================
         |	// Peripheral I/O addresses (dynamically allocated by IoAddressAllocator)
         |	// All addresses always defined; use HAS_* flags for runtime checks.
         |	// ====================================================================
         |
         |	/** System device base (alias for IO_BASE + SYS offset) */
         |	public static final int IO_SYS_DEVICE = IO_BASE + ${hexOffset(ioOffset(JopIoSpace.SYS_BASE))};
         |	/** Ethernet controller base address (MAC at +0, MDIO at +8) */
         |	public static final int IO_ETH = IO_BASE + ${devAddr("eth")};
         |	/** MDIO PHY management base address (ETH base + 8) */
         |	public static final int IO_MDIO = IO_ETH + 8;
         |	/** USB port (legacy alias — shares MDIO address slot) */
         |	public static final int IO_USB = IO_MDIO;
         |	public static final int IO_USB_STATUS = IO_USB;
         |	public static final int IO_USB_DATA = IO_USB + 1;
         |
         |	/** SD SPI base address */
         |	public static final int IO_SD_SPI = IO_BASE + ${devAddr("sdSpi")};
         |	/** VGA DMA framebuffer controller base address */
         |	public static final int IO_VGA_DMA = IO_BASE + ${devAddr("vgaDma")};
         |	/** SD Native base address */
         |	public static final int IO_SD = IO_BASE + ${devAddr("sdNative")};
         |	/** VGA text controller base address */
         |	public static final int IO_VGA = IO_BASE + ${devAddr("vgaText")};
         |	/** Config flash base address */
         |	public static final int IO_CFG_FLASH = IO_BASE + ${devAddr("cfgFlash")};
         |
         |	// Legacy aliases (referenced by toolchain and older apps)
         |	public static final int IO_STATUS2 = IO_BASE + 0x30;
         |	public static final int IO_UART2 = IO_BASE + 0x31;
         |""".stripMargin)

    // RTTM constants (referenced by ReplaceAtomicAnnotation in toolchain)
    sb.append(
      s"""
         |	// ====================================================================
         |	// RTTM constants (unused, retained for toolchain compatibility)
         |	// ====================================================================
         |
         |	public static final boolean USE_RTTM_BIGMEM = true;
         |	public static final int MEM_TM_MAGIC = USE_RTTM_BIGMEM ? 0x0C0000 : 0x060000;
         |	public static final int TM_END_TRANSACTION = 0;
         |	public static final int TM_START_TRANSACTION = 1;
         |	public static final int TM_ABORTED = 2;
         |	public static final int TM_EARLY_COMMIT = 3;
         |""".stripMargin)

    // Hardware availability and interrupts
    sb.append(
      s"""
         |	// ====================================================================
         |	// Hardware availability (derived from device config)
         |	// ====================================================================
         |
         |	/** Board has Ethernet PHY */
         |	public static final boolean HAS_ETHERNET = $hasEth;
         |	/** Board has SD card interface (SPI or Native) */
         |	public static final boolean HAS_SD_CARD = ${hasSdSpi || hasSdNative};
         |	/** Board has VGA output (DMA or Text) */
         |	public static final boolean HAS_VGA = ${hasVgaDma || hasVgaText};
         |	/** Board has config flash (EPCS/EPCQ) */
         |	public static final boolean HAS_CONFIG_FLASH = $hasCfgFlash;
         |
         |	// ====================================================================
         |	// Interrupt configuration
         |	// ====================================================================
         |
         |	/**
         |	 * Number of available interrupts: 1 timer + I/O device interrupts.
         |	 */
         |	public static final int NUM_INTERRUPTS = $numInterrupts;
         |}
         |""".stripMargin)

    sb.toString
  }
}

/** Generate Const.java from a JopConfig preset */
object ConstGeneratorMain extends App {
  import jop.system.JopTopVerilog

  val preset = args.headOption.getOrElse("ep4cgx150Serial")
  val writeToFile = args.contains("--write")
  val config = JopTopVerilog.resolvePreset(preset, args.filterNot(_ == "--write"))

  val source = ConstGenerator.generate(config)

  if (writeToFile) {
    val path = "java/runtime/src/jop/com/jopdesign/sys/Const.java"
    val writer = new java.io.PrintWriter(path)
    writer.print(source)
    writer.close()
    println(s"Wrote $path")
  } else {
    println(source)
  }
}
