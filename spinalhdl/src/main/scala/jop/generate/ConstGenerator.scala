package jop.generate

import jop.config._
import jop.memory.JopIoSpace
import jop.io.{DeviceTypes, IoAddressAllocator}

/**
 * Generates Const.java from JopConfig.
 *
 * Replaces the manually-maintained Const.java with a config-derived version.
 * I/O addresses for Sys and Uart are fixed (from JopIoSpace).
 * All other device addresses are computed by IoAddressAllocator.
 *
 * Usage:
 *   sbt "runMain jop.generate.ConstGeneratorMain ep4cgx150Serial"
 *   sbt "runMain jop.generate.ConstGeneratorMain wukongSdram --write"
 */
object ConstGenerator {

  /** Convert 8-bit I/O address to Java I/O offset from IO_BASE */
  private def ioOffset(addr8: Int): Int = addr8 - 0x80

  /** Format an I/O offset as hex string for readability */
  private def hexOffset(offset: Int): String =
    if (offset == 0) "0" else f"0x$offset%02x"

  /** Generate complete Const.java source */
  def generate(config: JopConfig): String = {
    // Union of all cores' configs across all systems
    val allCores = config.systems.flatMap(_.coreConfigs)

    // Union of all systems' device presence (works on either devices or ioConfig path)
    val hasEth = config.systems.exists(_.hasEth)
    val hasSdSpi = config.systems.exists(_.hasSdSpi)
    val hasSdNative = config.systems.exists(_.hasSdNative)
    val hasVgaDma = config.systems.exists(_.hasDevice("vgadma"))
    val hasVgaText = config.systems.exists(_.hasDevice("vgatext"))
    val hasCfgFlash = config.systems.exists(_.hasConfigFlash)

    // Build superset devices from all systems for address allocation
    val supersetDevices: Map[String, DeviceInstance] = config.systems.flatMap(_.effectiveDevices).toMap
    val bootName = DeviceTypes.bootDeviceName(supersetDevices)
    val allocatedDevices = IoAddressAllocator.allocate(
      DeviceTypes.toDescriptorsForAllocation(supersetDevices, bootName))
    val addrMap: Map[String, Int] = allocatedDevices.map(ad => ad.descriptor.name -> ad.baseAddr).toMap

    // Helper: get allocated base address for a device, or 0x80 (IO_BASE) as fallback
    def devAddr(name: String): String = addrMap.get(name) match {
      case Some(addr) => hexOffset(ioOffset(addr))
      case None => "0"
    }

    // Feature flags: true if ANY core uses Java (software) for that category
    val supportFloat = allCores.exists { c =>
      import Implementation.Java
      Seq(c.fadd, c.fsub, c.fmul, c.fdiv, c.fneg, c.i2f, c.f2i, c.fcmpl, c.fcmpg)
        .contains(Java)
    }

    // Number of interrupts: timer + max numIoInt across systems
    val maxNumIoInt = config.systems.map(sys => DeviceTypes.interruptCount(sys.effectiveDevices)).max
    val numInterrupts = 1 + maxNumIoInt

    val sb = new StringBuilder
    sb.append(
      s"""/*
         |  This file is part of JOP, the Java Optimized Processor
         |    see <http://www.jopdesign.com/>
