package java.lang;

public class NumberFormatException extends IllegalArgumentException {

	private static final long serialVersionUID = 6751378892046668104L;

	public NumberFormatException() {
	}

	public NumberFormatException(java.lang.String message) {
		super(message);
	}

	public NumberFormatException(java.lang.String message, java.lang.Throwable cause) {
		super(message, cause);
	}

	public NumberFormatException(java.lang.Throwable cause) {
		super(cause);
	}

}
